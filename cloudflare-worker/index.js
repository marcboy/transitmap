function arr(v) { return v == null ? [] : Array.isArray(v) ? v : [v]; }

function lookupParisStopById(ref) {
  const m = (ref ?? '').match(/Q:(\d+):/);
  return m ? (PARIS_STOPS_BY_ID[m[1]] ?? null) : null;
}

// Find the timetable segment containing `now` and return both the interpolated
// position AND the raw segment endpoints + timestamps so the client can
// re-interpolate continuously using Date.now() without needing another fetch.
// Returns {lat, lng, bearing, seg} or null.
// seg = { aLat, aLng, tDep, bLat, bLng, tArr } — absolute UTC ms timestamps.
function interpolateParisPosition(calls, now) {
  const coordsOf = call => {
    const ref = call?.StopPointRef?.value ?? call?.StopPointRef ?? '';
    return lookupParisStopById(ref);
  };
  const dep = c => parseTs(c.ExpectedDepartureTime) ?? parseTs(c.AimedDepartureTime)
                ?? parseTs(c.ExpectedArrivalTime)   ?? parseTs(c.AimedArrivalTime);

  // Max straight-line distance between two consecutive stops: ~1.4km.
  // Paris Metro average inter-station = 567m; longest known hop (La Défense →
  // Esplanade de la Défense on M1) ≈ 950m. PRIM sometimes skips intermediate
  // stops, creating pairs that span 3-4 physical stations — reject those.
  // 0.013° ≈ 1.45km latitude / 0.96km longitude at 48.8°N → safe upper bound.
  const MAX_DIST_SQ = 0.013 * 0.013;
  const validPair = (cA, cB) => {
    if (!cA || !cB) return false;
    const d = (cA[0]-cB[0])**2 + (cA[1]-cB[1])**2;
    return d <= MAX_DIST_SQ;
  };

  // Walk pairs of consecutive stops looking for the segment containing `now`
  for (let i = 0; i < calls.length - 1; i++) {
    const a = calls[i], b = calls[i + 1];
    const tDep = dep(a);
    const tArr = parseTs(b.ExpectedArrivalTime) ?? parseTs(b.AimedArrivalTime);
    if (!tDep || !tArr || tArr <= tDep) continue;
    if (tArr - tDep > 180000) continue; // skip multi-station gaps > 3 min
    if (now < tDep || now > tArr) continue;
    const cA = coordsOf(a), cB = coordsOf(b);
    if (!validPair(cA, cB)) continue; // skip multi-station distance jumps
    const t = (now - tDep) / (tArr - tDep);
    const lat = cA[0] + t * (cB[0] - cA[0]);
    const lng = cA[1] + t * (cB[1] - cA[1]);
    const bearing = Math.round(Math.atan2(cB[1] - cA[1], cB[0] - cA[0]) * 180 / Math.PI);
    return { lat, lng, bearing,
             seg: { aLat: cA[0], aLng: cA[1], tDep, bLat: cB[0], bLng: cB[1], tArr } };
  }

  // No active between-stop segment — find the first upcoming pair (tDep > now).
  for (let i = 0; i < calls.length - 1; i++) {
    const a = calls[i], b = calls[i + 1];
    const tDep = dep(a);
    const tArr = parseTs(b.ExpectedArrivalTime) ?? parseTs(b.AimedArrivalTime);
    if (!tDep || !tArr || tArr <= tDep) continue;
    if (tArr - tDep > 180000) continue;
    const cA = coordsOf(a), cB = coordsOf(b);
    if (!validPair(cA, cB)) continue; // same distance cap
    const t = Math.max(0, Math.min(1, (now - tDep) / (tArr - tDep)));
    return {
      lat: cA[0] + t*(cB[0]-cA[0]),
      lng: cA[1] + t*(cB[1]-cA[1]),
      bearing: Math.round(Math.atan2(cB[1]-cA[1], cB[0]-cA[0]) * 180/Math.PI),
      seg: { aLat: cA[0], aLng: cA[1], tDep, bLat: cB[0], bLng: cB[1], tArr },
    };
  }

  // Absolute fallback — no usable stops at all
  const c = coordsOf(calls[0]);
  return c ? { lat: c[0], lng: c[1], bearing: null, seg: null } : null;
}

async function fetchParisTrains(apiKey) {
  const resp = await withTimeout(
    fetch(IDFM_FEED, {
      headers: { 'apikey': apiKey, 'Accept': 'application/json' },
      cf: { cacheTtl: 30, cacheEverything: true },
    }), 12000
  );
  if (!resp.ok) throw new Error(`IDFM feed ${resp.status}`);

  const data = await resp.json();
  const now = Date.now();
  const trains = [];

  const deliveries = arr(data?.Siri?.ServiceDelivery?.EstimatedTimetableDelivery);
  for (const delivery of deliveries) {
    for (const frame of arr(delivery?.EstimatedJourneyVersionFrame)) {
      for (const journey of arr(frame?.EstimatedVehicleJourney)) {
        const lineRef = journey?.LineRef?.value ?? journey?.LineRef ?? '';
        const line = PARIS_LINES[lineRef];
        if (!line) continue;

        const vehicleRef  = journey?.VehicleRef?.value ?? journey?.VehicleRef ?? '';
        const journeyRef  = journey?.DatedVehicleJourneyRef?.value
                         ?? journey?.DatedVehicleJourneyRef
                         ?? journey?.VehicleJourneyRef?.value
                         ?? journey?.VehicleJourneyRef ?? '';
        // Sort by departure time — PRIM returns calls in stop-sequence order which
        // is often NOT chronological (trains on loop segments, bidirectional lines).
        // Without sorting, adjacent pairs fail tArr > tDep and no segment is found.
        const rawCalls = arr(journey?.EstimatedCalls?.EstimatedCall);
        rawCalls.sort((a, b) => {
          const tA = parseTs(a.ExpectedDepartureTime) ?? parseTs(a.AimedDepartureTime)
                  ?? parseTs(a.ExpectedArrivalTime)   ?? parseTs(a.AimedArrivalTime) ?? 0;
          const tB = parseTs(b.ExpectedDepartureTime) ?? parseTs(b.AimedDepartureTime)
                  ?? parseTs(b.ExpectedArrivalTime)   ?? parseTs(b.AimedArrivalTime) ?? 0;
          return tA - tB;
        });
        const calls = rawCalls.slice(0, 10);
        if (calls.length === 0) continue;

        const pos = interpolateParisPosition(calls, now);
        if (!pos) continue;

        trains.push({
          id:      vehicleRef || journeyRef || `${lineRef}-${trains.length}`,
          line:    line.name,
          color:   line.color,
          lat:     Math.round(pos.lat * 100000) / 100000,
          lng:     Math.round(pos.lng * 100000) / 100000,
          bearing: pos.bearing,
          status:  'IN_TRANSIT',
          source:  'timetable-interpolated',
          seg:     pos.seg,  // {aLat,aLng,tDep,bLat,bLng,tArr} for client-side clock animation
        });
      }
    }
  }

  return trains;
}

